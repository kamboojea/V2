// Function to initialize I2C



float SHT4x_ReadTemperature(void)
{
  uint8_t data[6];
  uint8_t command = 0xFD;
  float temperature;
  HAL_I2C_Master_Transmit(&hi2c2, 0x44 << 1, &command, 1, HAL_MAX_DELAY);

  HAL_Delay(100);

  HAL_I2C_Master_Receive(&hi2c2,0x44 << 1 ,data, 6, HAL_MAX_DELAY);

  float t_ticks = data[0]<<8 | data[1];
  temperature = (- 45.0 + (175.0 * t_ticks/ 65535.0)) ;

  return temperature;
}


float SHT4x_ReadHumidity(void)
{
  uint8_t data[6];
  uint8_t command = 0xFD;
  float humidity;

  HAL_I2C_Master_Transmit(&hi2c2, 0x44 << 1, &command, 1, HAL_MAX_DELAY);

    HAL_Delay(100);

    HAL_I2C_Master_Receive(&hi2c2,0x44 << 1 ,data, 6, HAL_MAX_DELAY);

    humidity = ((100.0 * (float)((data[3] << 8) | data[4])) / 65535.0);

  return humidity;
}
void i2c_scanner(void)
{
	uint8_t Buffer[25] = {0};
	uint8_t Space[] = " - ";
	uint8_t StartMSG[] = "Starting I2C Scanning: \r\n";
	uint8_t EndMSG[] = "Done! \r\n\r\n";
	int i = 0x00,ret;


	/*-[ I2C Bus Scanning ]-*/
	  HAL_UART_Transmit(&huart1, StartMSG, sizeof(StartMSG), 10000);
	    for( i=1; i<128; i++)
	    {
	    	HAL_StatusTypeDef ret = HAL_I2C_IsDeviceReady(&hi2c2, (uint16_t)(i<<1), 3, 5);
	        if (ret != HAL_OK) /* No ACK Received At That Address */
	        {
	            HAL_UART_Transmit(&huart1, Space, sizeof(Space), 10000);
	        }
	        else if(ret == HAL_OK)
	        {
	            sprintf(Buffer, "0x%X", i);
	            HAL_UART_Transmit(&huart1, Buffer, sizeof(Buffer), 10000);
	        }
	    }
	    HAL_UART_Transmit(&huart1, EndMSG, sizeof(EndMSG), 10000);
	    /*--[ Scanning Done ]--*/
}

